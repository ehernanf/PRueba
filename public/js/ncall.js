function showMessage(tipoMensaje, mensaje) {
	color = "#659265";
	icono = "fa fa-check fa-2x fadeInRight animated";
	if (tipoMensaje == "error") {
		color = "#C46A69";
		icono = "fa fa-times fa-2x fadeInRight animated";
	} else if (tipoMensaje == "info") {
		color = "#eeba30";
		icono = "fa fa-warning bounce animated";
	}
	$.smallBox({
		title : tipoMensaje,
		content : "<i class='fa fa-clock-o'></i> <i>"+mensaje+"</i>",
		color : color,
		iconSmall : icono,
		timeout : 4000
	});
}

